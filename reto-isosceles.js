function isosceles (lado1, lado2, base) {
    if (lado1 == lado2) {
        
        const height = Math.sqrt(lado1 ** 2 - base ** 2 /4);
        alert("Soy un isósceles y mi altura es: " + height);
    } else {
        alert("No soy un triángulo isósceles");
    }
}

